const ROUTES_BY_ID = new Map(INITIAL_ROUTE_CATALOG.map((route) => [route.routeId, route]));

function buildAssignedRoute(routeId: string): DriverAssignedRoute {
  const route = ROUTES_BY_ID.get(routeId);

  if (!route) {
    throw new Error(`No se encontro la ruta ${routeId} para poblar conductores mock.`);
  }

  return {
    routeId: route.routeId,
    idRuta: route.idRuta,
    nombreRuta: route.nombreRuta,
    zona: route.zona,
    estado: 'ACTIVO',
  };
}

